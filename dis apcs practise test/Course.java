public class Course
{
    private String name;
    private String teacher;
    public Course( String name, String teacher )
    {
        this.name = name;
        this.teacher = teacher;
    }//end method Course
    public String getName()
    {
        return name;
    }//end method getName
    public String getTeacher()
    {
        return teacher;
    }//end method getTeacher
}//end class Course